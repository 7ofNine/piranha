#include "../src/manipulators/fs.h"

using namespace piranha;

typedef fs stype;

// Power two of elp3 series without truncation.
int main()
{
  stype::set_truncation(0);
  stype elp3("elp3.csv"), elp3a(elp3);
  elp3*=elp3a;
  elp3*=elp3a;
  elp3*=elp3a;
  return 0;
}
